
public class Figura262
  
{
    // Método estático para calcular el Perímetro
    public static double Perimetro(double lado1, double lado2, double Base) 
    {
         return lado1+lado2+Base;
    }

    // Método estático para calcular el Área
    public static double Area(double Base, double altura) 
    {
         return  (Base * altura)/2;
    }

    // Metodo estatico para calcular el Volumen 
    public static double Volumen(double area, double Altura)
     {
          return (area * Altura);
     }

}